
public class DesafioPoundsToKg {
	
	public static void main(String[] args) {
		// converter pounds em kg
		
		// 1 pound = 0.45359237
		
		int nroPounds = 200;
		double poundsToKg = nroPounds * 0.45359237d;
		
		System.out.println(nroPounds + " são " + poundsToKg + " kgs");
		
		double pi = 3.14515927d;
		double anotherNumber = 3_000_000.4_567_890d;
		System.out.println(pi);
		System.out.println(anotherNumber);
	}

}
